// traductor palabra

let palabra = "puerta";

switch(palabra){
    case "perro":
        console.log("Dog");
        break;
    case "gato":
        console.log("Cat");
        break;
    case "puerta":
        console.log("Door");
        break;
    case "ventana":
        console.log("Window");
        break;
    case "mesa":
        console.log("Table")
    default:
        console.log("Very dificult");
        break;
}

